package pojo;

/**
 *
 * @author Laboratorios
 */
public class Specialization {

    private String name;
    private int level;
    private int time;

    public Specialization() {
    }

    public Specialization(String name, int level, int time) {
        this.name = name;
        this.level = level;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

}
